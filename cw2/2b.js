let month = 12;
if (month > 0 && month <= 3){
    console.log("Zima");
} else if (month > 3 && month <= 6) {
    console.log("Wiosna");
} else if (month > 6 && month <= 8) {
    console.log("Lato");
} else if (month > 8 && month <= 12) {
    console.log("Jesień");
} else {
    console.log("Zły miesiąc.");
}